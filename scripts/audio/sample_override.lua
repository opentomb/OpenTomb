function GetSample(ver, id)
	sample = {};

    if(ver < 3) then                    -- TR_I, TR_I_DEMO, TR_I_UB
		sample = tr1_sample;
		path   = tr1_sample_path;
    else
		print("ERROR - no sample loaded");
        return "NONE";
    end;
	
	if(sample[id] == nil) then
		print("ERROR - no table entry found");
        return nil;
    else
        return (path .. sample[id].file);
    end;
end;

